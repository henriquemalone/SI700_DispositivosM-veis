package p169624.ft.unicamp.br.aula02.jogo3;

public class Pontuacao {
    String nome;
    float erros;
    float acertos;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public float getErros() {
        return erros;
    }

    public void setErros(float erros) {
        this.erros = erros;
    }

    public float getAcertos() {
        return acertos;
    }

    public void setAcertos(float acertos) {
        this.acertos = acertos;
    }
}
